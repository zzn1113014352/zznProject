package com.dhcc.bussiness.alluser;

public class All_userModel {
	private String id;//id
	private String name;//姓名
	private String idcard;//身份证号
	private String phone;//联系方式
	private String shenfen_renzheng;//身份认证
	private String c_time;//创建日期
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIdcard() {
		return idcard;
	}
	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getShenfen_renzheng() {
		return shenfen_renzheng;
	}
	public void setShenfen_renzheng(String shenfen_renzheng) {
		this.shenfen_renzheng = shenfen_renzheng;
	}
	public String getC_time() {
		return c_time;
	}
	public void setC_time(String c_time) {
		this.c_time = c_time;
	}
}